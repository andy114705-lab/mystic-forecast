/** 真太阳时修正（分钟） */
export function trueSolarMinutes(inputHour, inputMinute, longitude) {
  const totalClockMinutes = inputHour * 60 + inputMinute;
  // 东八区基准经度 120°E，每偏离 1° 修正 4 分钟
  const corrected = totalClockMinutes + (longitude - 120) * 4;
  // 控制在 0-1439 之间（一天）
  let minutes = Math.round(corrected % 1440);
  if (minutes < 0) minutes += 1440;
  return { hour: Math.floor(minutes / 60), minute: minutes % 60, offset: Math.round((longitude - 120) * 4) };
}
